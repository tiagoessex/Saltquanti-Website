                    <p>
                        Investigadores da Faculdade de Engenharia (FEUP) e da Faculdade de Ciências da Nutrição e Alimentação (FCNAUP) da Universidade do Porto em parceria com a EVOLEO Technologies, Lda. realizaram uma sessão de auscultação de parceiros do projeto Salt Quanti, cofinanciado no âmbito Fundo Europeu de Desenvolvimento Regional (FEDER), através do Programa Operacional Regional do Norte (NORTE2020), no dia 26 de setembro nas instalações da FEUP.
                    </p>
                    <p>
                        O consumo de sal é um dos principais problemas de Saúde Pública no Mundo. Portugal é um dos países em que esta questão se assume como alarmante com uma ingestão diária per capita de 10,7 g, mais do dobro da recomendação preconizada pela Organização Mundial de Saúde (OMS).
                    </p>
                    <p>
                        Reconhecendo o impacto nocivo do consumo excessivo de sal no aumento da pressão arterial e mortalidade/morbilidade por doença cardiovascular, a maior causa de mortalidade em Portugal, a OMS definiu o objetivo de redução do consumo de sal em 30% até 2025, aconselhando os países a adotarem estratégias de monitorização e redução da oferta alimentar. A redução do consumo de sal é apontada como uma das prioridades de saúde, nomeadamente para a sustentabilidade económica dos sistemas de saúde. Os investigadores acreditam que o desenvolvimento do equipamento Salt Quanti, um medidor portátil de sal em alimentos, poderá ser um auxiliar na monitorização e redução de alimentos com elevado teor de sal pela indústria alimentar e restauração.
                    </p>
                    <p>
                        A sessão teve como enfoque a apresentação do projeto de co-promoção e dos passos de desenvolvimento do equipamento. Estiveram presentes vários parceiros nomeadamente o Ministério da Saúde, Ordem dos Nutricionistas, DGS, XXX, DGEsT, ACIP, Centro Hospitalar São João, Centro Hospitalar Trás os Montes e Alto Douro, ULSAM, ARS Algarve, ARS Centro, ACES Baixo Mondego, ACES Porto Oriental, Câmara Municipal Vila Nova de Gaia, Câmara Municipal Porto, Câmara Municipal Matosinhos, Câmara Municipal Maia, AHRESP, Eurest e Gertal.
                    </p>
                    <p>
                        A reunião foi um espaço de esclarecimento e debate sobre o equipamento Salt Quanti. Todos os participantes debateram a importância da monitorização do sal e reconheceram a utilidade deste equipamento nas intervenções de redução das suas entidades. O Salt Quanti foi reconhecido como um instrumento de máximo interesse para dar resposta ao controlo de sal em alimentos. Para além da prospeção de necessidades dos parceiros, a sessão serviu também para envolvimento das entidades nas próximas etapas do projeto que incluem a validação do equipamento em ambiente real de diferentes naturezas, como cozinhas de escolas, hospitais e empresas a ser realizado 2018.
